# Database

This folder contains the SQL scripts required to recreate the fictional support ticket database used throughout this project.

## Files

- **schema.sql** – Defines the `tickets` table structure.
- **sample_data.sql** – Inserts 20 fictional support tickets.
- **full_database.sql** – Creates the database, table, and sample data in a single script.

The dataset is fictional and was created solely for demonstration and portfolio purposes.
